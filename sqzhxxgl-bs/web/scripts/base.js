function swap(swapId)
{
	if($("#"+swapId).css("display")=='none'){
		$("#"+swapId).show();
	}else{
		$("#"+swapId).hide();
	}
}



function openWin(url)
{
	if(null!=url && url!="")
	{
		　window.open (url, "", "height=500, width=800, toolbar =no, menubar=no, scrollbars=no, resizable=no, location=no, status=no")
	}
}


